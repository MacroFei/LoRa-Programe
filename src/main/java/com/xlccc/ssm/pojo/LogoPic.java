package com.xlccc.ssm.pojo;

public class LogoPic {
    private int id ;
    private String title ;
    private String logo;
    private String logoSetting;
    private String gif;
    private String pic ;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }

    public String getLogo() {
        return logo;
    }

    public void setLogo(String logo) {
        this.logo = logo;
    }

    public String getLogoSetting() {
        return logoSetting;
    }

    public void setLogoSetting(String logoSetting) {
        this.logoSetting = logoSetting;
    }

    public String getGif() {
        return gif;
    }

    public void setGif(String gif) {
        this.gif = gif;
    }

    public String getPic() {
        return pic;
    }

    public void setPic(String pic) {
        this.pic = pic;
    }
}
